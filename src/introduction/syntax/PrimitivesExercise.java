package introduction.syntax;

public class PrimitivesExercise {

    public static void main(String[] args) {
        //primitive
        int x;          //variabile - declarare variabila
                        //int = primitiva de tip numeric
        x = 10;         //initializare, asignare

        int y = 12;     //declarare + asignare valoare
        y = 13;
        System.out.println("Int y has value: " + y);

        byte b = 100;
        System.out.println("Byte b has value: " + b);

        short s = 2000;
        System.out.println("Short s has value:" + s);

        long l = 9999999999999999L;
        System.out.println("Long l has value: " + l);

        double d = 10.5;
        System.out.println("Double d has value: " + d);

        float f = 10.5F;
        System.out.println("Float f has value: " + f);

        boolean b1 = true;
        boolean b2 = false;
        System.out.println("Boolean b1 and b2 has value: " + b1 + b2);

        char c1 = 'a';
        System.out.println("Character c1 has value:" + c1);
        char c2 = '\n';         //newline
        System.out.println("Character c1: " + c1 + c2 + "newline");

        int a1, a2 = 1, a3;

    }
}
